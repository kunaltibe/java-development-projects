    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtSearch.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Search field is empty. Please enter an Account Number.", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Account result = accountdirectory.searchAccount(txtSearch.getText());
                if(result == null || !(accountdirectory.getAccountList().contains(result))){
                    JOptionPane.showMessageDialog(null,"Account number doesn't exists","Information",JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    ViewAccountJPanel panel = new ViewAccountJPanel(workPanel, result );
                    workPanel.add("ViewAccountJPanel", panel);
                    CardLayout layout = (CardLayout) workPanel.getLayout();
                    layout.next(workPanel);
                }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

